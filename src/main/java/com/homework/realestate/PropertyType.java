package com.homework.realestate;

/**
 * Enumeration of different types of real estate properties.
 * This enum defines the various categories that a property can belong to.
 */
public enum PropertyType {
    /** Represents an apartment property */
    APARTMENT,
    
    /** Represents a house property */
    HOUSE,
    
    /** Represents a condominium property */
    CONDO,
    
    /** Represents a townhouse property */
    TOWNHOUSE
}